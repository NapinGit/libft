#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		lens1;
	char	*dest;

	i = 0;
	lens1 = 0;
	while (s1[lens1] != '\0')
		lens1++;
	if (!(dest = (char *)malloc(sizeof(*dest) * lens1 + 1)))
		return (NULL);
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
